const mongoose = require('mongoose');

const EmployerSchema = new mongoose.Schema({
    fullName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phoneNumber: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
    
});

mongoose.model("EmployerModel", EmployerSchema);